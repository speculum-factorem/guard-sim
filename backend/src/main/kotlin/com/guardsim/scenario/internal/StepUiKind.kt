package com.guardsim.scenario.internal

enum class StepUiKind {
    /** Обычный текст + кнопки ответов */
    GENERIC,

    /** Рамка почтового клиента + опционально хотспоты */
    EMAIL_CLIENT,

    /** Карточка уведомления соцсети */
    SOCIAL_NOTIFICATION,

    /** Тикет / задание ИБ */
    DESK_TICKET,

    /** Мини-игра: два URL на выбор (привязка к идентификатору варианта ответа) */
    MINI_URL_COMPARE,

    /** Мессенджер: короткие сообщения, пересылка из канала */
    CHAT_MESSENGER,

    /** Приглашение во встречу (календарь): тема, время, описание со ссылкой */
    CALENDAR_INVITE,

    /** Карточка расширения браузера / магазина приложений */
    EXTENSION_STORE,
}
